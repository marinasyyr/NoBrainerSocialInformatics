const FACT_COLORS = ['pink', 'teal', 'purple', 'amber', 'cyan', 'rose']

export function FunFacts() {
  const [expanded, setExpanded] = useState(false)
  const visibleFacts = expanded ? FUN_FACTS : FUN_FACTS.slice(0, 2)

  return (
    <section className="fun-facts" aria-label="Fun facts">
      <div className="fun-facts-card">
        <h2 className="fun-facts-title">Fun facts</h2>
        <p className="fun-facts-subtitle">Ξέρετε τόσα για τον εγκέφαλο;</p>
        <div className="fun-facts-list">
          {visibleFacts.map((text, i) => (
            <article
              key={i}
              className={`fun-facts-item fun-facts-item--${FACT_COLORS[i % FACT_COLORS.length]}`}
            >
              <span className="fun-facts-number" aria-hidden="true">
                {i + 1}
              </span>
              <p className="fun-facts-text">{text}</p>
            </article>
          ))}
        </div>
        {FUN_FACTS.length > 2 && (
          <button
            type="button"
            className="fun-facts-toggle"
            onClick={() => setExpanded((prev) => !prev)}
            aria-expanded={expanded}
          >
            {expanded ? 'Εμφάνιση λιγότερων' : `Δείτε όλα τα ${FUN_FACTS.length} στοιχεία`}
          </button>
        )}
      </div>
    </section>
  )
}
