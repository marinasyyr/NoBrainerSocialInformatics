import { useState } from 'react'
import './NeuroscienceIntro.css'

const FACTS = [
  'Ο ανθρώπινος εγκέφαλος έχει περίπου 86 δισεκατομμύρια νευρώνες και περίπου τον ίδιο αριθμό γλοιακών κυττάρων.',
  'Οι νευρώνες επικοινωνούν μέσω ηλεκτρικών και χημικών σημάτων στα συνάψεις.',
  'Η νευροπλαστικότητα επιτρέπει στον εγκέφαλο να αναδιοργανώνεται και να σχηματίζει νέες συνδέσεις σε όλη τη ζωή.',
  'Ο εγκέφαλος καταναλώνει περίπου 20% της συνολικής ενέργειας του σώματος παρά το ότι είναι ~2% του σωματικού βάρους.',
]

export function NeuroscienceIntro() {
  const [expanded, setExpanded] = useState(false)

  return (
    <section className="neuro-intro" aria-label="Εισαγωγή στη νευροεπιστήμη">
      <div className="neuro-intro-card">
        <h2>Εγκέφαλος &amp; Νευροεπιστήμη</h2>
        <p>
          Ο εγκέφαλος είναι το κέντρο ελέγχου του σώματος: επεξεργάζεται αισθητηριακά ερεθίσματα, ρυθμίζει τη σκέψη, τη μνήμη και το συναίσθημα και συντονίζει την κίνηση και τη συμπεριφορά. Η νευροεπιστήμη μελετά πώς λειτουργούν το νευρικό σύστημα και ο εγκέφαλος σε μοριακό, κυτταρικό και συστημικό επίπεδο. Η κατανόηση των περιοχών του εγκεφάλου βοηθά να δούμε πώς διαφορετικές ζώνες υποστηρίζουν τη γνωστική λειτουργία, την αντίληψη και τη δράση.
        </p>
        <button
          type="button"
          className="neuro-intro-toggle"
          onClick={() => setExpanded((prev) => !prev)}
          aria-expanded={expanded}
        >
          {expanded ? 'Λιγότερα' : 'Περισσότερα στοιχεία'}
        </button>
        {expanded && (
          <ul className="neuro-facts">
            {FACTS.map((fact, i) => (
              <li key={i}>{fact}</li>
            ))}
          </ul>
        )}
      </div>
    </section>
  )
}
